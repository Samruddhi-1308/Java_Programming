public class swap_no
{
    public static void main(String[]args)
    {
        int a=10,b=15,temp;
        System.out.println("number before swapping");
        System.out.println(a);
        System.out.println(b);

        
        temp=a;
        a=b;
        b=temp;
    

        System.out.println("number after swapping");
        System.out.println(a);
        System.out.println(b);



    }
    
}
